/** 입출력 받아보기
 *	5 - 10172번: 그림을 출력하는 문제
 *  '"'(큰따옴표)와 '\'(백슬래시)앞에 '\'를 한번더 붙여준다
 */

package ch1;

public class ch1_5 {
	   public static void main(String[] args){
	        System.out.println("|\\_/|");
	        System.out.println("|q p|   /}");
	        System.out.println("( 0 )\"\"\"\\");
	        System.out.println("|\"^\"`    |");
	        System.out.println("||_/=\\\\__|");
	    }
	}
//"|\_/|"		-> |\\_/|
//"|q p|   /}"	-> |q p|   /}
//"( 0 )"""\"	-> ( 0 )\"\"\"\\
//"|"^"`    |"  -> |\"^\"`    |
//"||_/=\\__|"  -> ||_/=\\\\__|